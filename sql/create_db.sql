SET storage_engine=InnoDB;
SET FOREIGN_KEY_CHECKS=1;

DROP DATABASE IF EXISTS registrogrest;
CREATE DATABASE IF NOT EXISTS registrogrest;
USE registrogrest;

DROP TABLE IF EXISTS partecipanti;
DROP TABLE IF EXISTS staff;
DROP TABLE IF EXISTS lavoratori;
DROP TABLE IF EXISTS programma;
DROP TABLE IF EXISTS squadre;
DROP TABLE IF EXISTS utenti;
DROP TABLE IF EXISTS giorni;
DROP TABLE IF EXISTS ruoli;

CREATE TABLE utenti (
	ID INT UNSIGNED NOT NULL AUTO_INCREMENT,
	Nome CHAR(30) NOT NULL,
	Cognome CHAR(30) NOT NULL,
	PRIMARY KEY (ID)
);

CREATE TABLE ruoli (
	ID TINYINT NOT NULL AUTO_INCREMENT,
	servizio CHAR(50) NOT NULL,
	livello TINYINT NOT NULL,
	PRIMARY KEY (ID, servizio, livello)
);

CREATE TABLE staff (
	ID INT UNSIGNED NOT NULL,
	Nickname CHAR(20) NOT NULL,
	Password CHAR(64) NOT NULL,
	ruolo TINYINT NOT NULL,
	PRIMARY KEY (ID, Nickname), 
	FOREIGN KEY (ID)
		REFERENCES utenti(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE,
	FOREIGN KEY (ruolo)
		REFERENCES ruoli(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE 
);

CREATE TABLE squadre (
	ID INT UNSIGNED NOT NULL AUTO_INCREMENT,
	Nome CHAR(60) NOT NULL,
	Anno SMALLINT UNSIGNED NOT NULL,
	PRIMARY KEY (ID)
);

CREATE TABLE lavoratori (
	ID INT UNSIGNED NOT NULL,
	Squadra INT UNSIGNED NOT NULL,
	Anno SMALLINT UNSIGNED NOT NULL,
	ruolo TINYINT NOT NULL,
	PRIMARY KEY (ID, Anno),
    FOREIGN KEY (ID)
		REFERENCES utenti(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE,
	FOREIGN KEY (Squadra)
		REFERENCES squadre(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE
	FOREIGN KEY (ruolo)
		REFERENCES ruoli(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE
);

CREATE TABLE partecipanti (
	ID INT UNSIGNED NOT NULL,
    Squadra INT UNSIGNED NOT NULL,
	Anno SMALLINT NOT NULL,
	Accompagnatore CHAR(60) NOT NULL,
	PRIMARY KEY (ID),
    FOREIGN KEY (ID)
		REFERENCES utenti(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE,
    FOREIGN KEY (Squadra)
		REFERENCES squadre(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE
);

CREATE TABLE programma (
	Data DATE NOT NULL,
	Descrizione TEXT,
	ID INT UNSIGNED NOT NULL,
	PRIMARY KEY (Data),
	FOREIGN KEY (ID)
		REFERENCES utenti(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE
);

CREATE TABLE giorni (
	Data DATE NOT NULL,
	ID INT UNSIGNED NOT NULL,
	Temperatura DOUBLE(2, 1),
	Assente BIT(1) NOT NULL,
	MotivoAssenza TEXT,
	PRIMARY KEY (Data), 
    FOREIGN KEY (ID)
		REFERENCES utenti(ID) 
		ON DELETE CASCADE
		ON UPDATE CASCADE
);